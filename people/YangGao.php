          <font size="3" face="times new roman"><strong>Education:</strong><br/>Ph.D. Candidate, Environmental Engineering, University of Tennessee, Knoxville,
2008 ~ Present.</p><br/>
          <font size="3" face="times new roman"><strong>Research Interests:</strong><br/>
       
           &#8226; <strong>Global and Regional Climate and Chemistry Downscaling</strong><br/>
Apply innovative tools and techniques to assess the impact of climate change, especially changes in extreme weather events, on future air quality in both global and regional scales.
<br/><br/>

           &#8226;<strong>Study of Extreme Weather Events</strong><br/>
Investigate global extreme weather events based on CMIP 5 RCP scenarios, and downscale global climate to regional scale (up to 4 km by 4 km) to evaluate regional extreme events
<br/><br/>

          &#8226; <strong>Biomass Burning Impact from Southeast Asia</strong><br/>
Study the long-range transport impact of emissions from biomass burning in Southeast Asia on downwind regions.
<br/><br/>

          &#8226; <strong>Emission Controls in Yangtze River Delta, China</strong><br/>
Focus on emission control strategies from large power plants and vehicles in megacities and improve the air quality in Yangtze River Delta, China.
<br/><br/>

          &#8226; <strong>Source-Receptor Case Study</strong><br/>
Examine the mechanism of intercontinental transport of air pollutants.</p><br/>     
            
